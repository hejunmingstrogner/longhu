const {ccclass, property} = cc._decorator;

@ccclass
export default class NewClass extends cc.Component {

    @property(cc.SpriteFrame)
    pockerDian:cc.SpriteFrame[] = null
    

    // 设置扑克的点数信息

    setPockerValue(value){

        if(value > 0 && value <= 15){

            this.getComponent(cc.Sprite).spriteFrame = this.pockerDian[value - 1]
        }

    }


}
